public class Mod {
    public static void main(String[] args){
        // x % y -> remainder after x / y
        // If you have expression like 100 / 9, you think
        // "How many times does 9 go into 100, then what is left over?"
        System.out.println(3.5 % 4); // 3.5 - 4 goes in 3.5 0 times, 3.5 left over
        System.out.println(4 % 3.5); // 0.5 - 3.5 goes in 4 1 time, 0.5 left over
    }

}
